'''
This class does one thing...gets you the list of adjacent coordinates or an error
that they don't exist. There are other classes for getting you the vx,vy,vz information
this simply gets you the valid coordinate space around a "centroid", so you know
the order of the 375 floats that were encoded.
The unit tests below show you EXACTLY what the output is expected to be
and traces through two examples for two different experiments.
'''


class adjacent_coordinate:
    file = None
    key = None

    def __init__(self, json_file_location, key):
        self.file = json_file_location
        self.key = key
        with open(json_file_location, 'r') as file:
            json_data = json.load(file)
        self.data_cache = json_data
    def load_data(self, key: str):
        with open(self.json_file_location, 'r') as file:
            json_data = json.load(file)
        if key not in json_data:
            raise ValueError(f"Key {key} not found in JSON file.")

        self.data_cache[key] = {
            'x_enumerated': json_data[key]['x_enumerated'],
            'y_enumerated': json_data[key]['y_enumerated'],
            'z_enumerated': json_data[key]['z_enumerated']
        }

    def locate_adjacent_values(self, value: int, enumerated_list: List[int]) -> List[int]:
        sorted_list = sorted(enumerated_list)
        index = sorted_list.index(value)
        if index < 2 or index >= len(sorted_list) - 2:
            raise ValueError("Coordinate not found")
        return sorted_list[index - 2:index + 3]

    def find_adjacent_coordinates(self, x: int, y: int, z: int) -> List[List[int]]:
        x_values = self.locate_adjacent_values(x, self.data_cache[self.key]['x_enumerated']) #[-125.0, -121.0, -117.0, -113.0, -109.0]
        y_values = self.locate_adjacent_values(y, self.data_cache[self.key]['y_enumerated']) #[71.0, 75.0, 79.0, 83.0, 87.0]
        z_values = self.locate_adjacent_values(z, self.data_cache[self.key]['z_enumerated']) #[-33.0, -29.0, -25.0, -21.0, -17.0]
