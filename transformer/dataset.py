import torch
import h5py
from torch.utils.data import Dataset

class TransformerDataset(Dataset):
    """
    A PyTorch Dataset for loading samples from the HDF5 files generated by Ordered_010_Prepare_Dataset.py.
    Each sample is a tensor of shape (num_time, 26, 52) containing:
    - 47 latent features
    - x, y, z coordinates
    - relative time
    - parameter value (e.g., 7.8)
    """
    def __init__(self, h5_path):
        self.h5_path = h5_path
        self._file = None
        
        # Open once to get length
        with h5py.File(self.h5_path, 'r') as f:
            self.length = f['data'].shape[0]
            self.feature_description = f.attrs.get('feature_description', "Unknown")

    def __len__(self):
        return self.length

    def __getitem__(self, idx):
        # We open the file lazily in each process to be multiprocessing-safe
        if self._file is None:
            self._file = h5py.File(self.h5_path, 'r')
        
        # Load the sample
        data = self._file['data'][idx]
        
        # Convert to torch tensor
        return torch.from_numpy(data)

    def close(self):
        if self._file is not None:
            self._file.close()
            self._file = None

if __name__ == "__main__":
    # Example usage
    import os
    import argparse
    from transformer_config import add_config_arg, load_config, resolve_path

    parser = argparse.ArgumentParser(description="Inspect a transformer HDF5 dataset.")
    add_config_arg(parser)
    parser.add_argument("--h5", default=None, help="HDF5 file to inspect.")
    args = parser.parse_args()
    cfg = load_config(args.config)
    h5_file = resolve_path(args.h5 or cfg["paths"]["training_h5"])
    if os.path.exists(h5_file):
        dataset = TransformerDataset(h5_file)
        print(f"Dataset length: {len(dataset)}")
        sample = dataset[0]
        print(f"Sample shape: {sample.shape}")
        print(f"Feature description: {dataset.feature_description}")
    else:
        print(f"File {h5_file} not found. Run the preparation script first.")
