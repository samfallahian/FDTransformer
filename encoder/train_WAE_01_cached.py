def _load_cached_array(file_path: str, limit: Optional[int] = None) -> np.ndarray:
    """Load a pickled NumPy array from `file_path` and optionally cap rows.

    Ensures dtype float32 for compact storage and faster training.
    """
    t0 = time.time()
    with open(file_path, 'rb') as f:
        arr = pickle.load(f)
    if not isinstance(arr, np.ndarray):
        raise TypeError(f"Cached file {file_path} does not contain a NumPy array; got {type(arr)}")
    if arr.dtype != np.float32:
        arr = arr.astype(np.float32, copy=False)
    if limit is not None:
        arr = arr[:int(limit)]
    t1 = time.time()
    logger.info(f"Loaded cached array: {file_path} shape={arr.shape} dtype={arr.dtype} in {t1 - t0:.2f}s")
    return arr


def _make_loader(
        arr: np.ndarray,
        batch_size: int,
        shuffle: bool,
        num_workers: int = 0,
        prefetch_factor: int = 2,
        persistent_workers: bool = False,
        pin_memory: bool = True,
    ) -> DataLoader:
    """Wrap a NumPy array into a TensorDataset/DataLoader.

    - Uses pinned memory tensors on CUDA for faster H2D transfers.
    - Drop last is False to keep as many samples as possible per epoch.
    - Supports multi-worker prefetch to keep the GPU fed.
    """
    # Create a torch tensor view (copies once from NumPy into a single big tensor)
    x_tensor = torch.from_numpy(arr)  # float32 ensured earlier
    dataset = TensorDataset(x_tensor)

    # persistent_workers is only valid when num_workers > 0
    use_persistent = bool(persistent_workers and num_workers > 0)

    # On CPU-only runs, pin_memory has no effect; on CUDA it accelerates H2D
    loader_kwargs = dict(
        batch_size=batch_size,
        shuffle=shuffle,
        pin_memory=pin_memory,
        drop_last=False,
        num_workers=max(0, int(num_workers)),
        persistent_workers=use_persistent,
    )
    if int(num_workers) > 0:
        loader_kwargs['prefetch_factor'] = int(prefetch_factor)
    loader = DataLoader(
        dataset,
        **loader_kwargs,
    )
    return loader


