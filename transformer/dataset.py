import torch
import h5py
from torch.utils.data import Dataset

class TransformerDataset(Dataset):
    """
    A PyTorch Dataset for loading samples from the HDF5 files generated by Ordered_010_Prepare_Dataset.py.
    Each sample is a tensor of shape (8, 26, 52) containing:
    - 47 latent features
    - x, y, z coordinates
    - relative time (0-7)
    - parameter value (e.g., 7.8)
    """
    def __init__(self, h5_path):
        self.h5_path = h5_path
        self._file = None
        
        # Open once to get length
        with h5py.File(self.h5_path, 'r') as f:
            self.length = f['data'].shape[0]
            self.feature_description = f.attrs.get('feature_description', "Unknown")

    def __len__(self):
        return self.length

    def __getitem__(self, idx):
        # We open the file lazily in each process to be multiprocessing-safe
        if self._file is None:
            self._file = h5py.File(self.h5_path, 'r')
        
        # Load the sample
        data = self._file['data'][idx]
        
        # Convert to torch tensor
        return torch.from_numpy(data)

    def close(self):
        if self._file is not None:
            self._file.close()
            self._file = None

if __name__ == "__main__":
    # Example usage
    import os
